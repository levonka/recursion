#include <stdio.h>

void	ft_pow_of_2(int n)
{
	if (n % 2 == 0 && n > 0)
		ft_pow_of_2(n / 2);
	else if (n == 1)
	{
		printf("YEEEES\n");
		return ;
	}
	else
		printf("NO\n");
}

int		main()
{
	int		i;

	i = 0;
	while (i <= 64)
	{
		printf("%d === ", i);
		ft_pow_of_2(i);
		++i;
	}
	return 0;
}

